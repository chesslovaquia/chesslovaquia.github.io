import { Clvq } from './Clvq';

function showSystemInfo(pre: HTMLPreElement): void {
	pre.textContent += `window.innerWidth: ${window.innerWidth}\n`;
	pre.textContent += `window.innerHeight: ${window.innerHeight}\n`;
}

// Initialize when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
	console.log('Welcome to Chesslovaquia.');
	(window as any).Clvq = new Clvq();

	const systemInfo = document.getElementById('systemInfo');
	if (systemInfo) {
		showSystemInfo(systemInfo);
	}
})
